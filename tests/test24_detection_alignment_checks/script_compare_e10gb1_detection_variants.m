function filtered_cache = local_precompute_filtered_bands(state_var, Fs, params)
n_bands = size(params.passband, 1);
filtered_cache = cell(n_bands, 1);

for b = 1:n_bands
    fprintf('  Filtering band %d/%d at Fs = %.12g Hz ...\n', b, n_bands, Fs);
    filtered_cache{b} = transpose(double(filterSignal_mirror( ...
        transpose(state_var), ...
        Fs, ...
        params.passband(b, 1), ...
        params.passband(b, 2), ...
        1)));
end
end


function result = local_detect_from_filtered( ...
    variant_name, data_source, filtered_cache, state_var, dx, Fs, params, session_lengths, enforce_segment_bounds)
n_bands = size(params.passband, 1);
n_channels = size(state_var, 1);
L_total = size(state_var, 2);

[session_start_idx, session_end_idx] = local_resolve_session_bounds(L_total, session_lengths);
DetectResults = cell(n_bands, n_channels);

for b = 1:n_bands
    filtered_band = filtered_cache{b};

    for c = 1:n_channels
        trace_all = double(filtered_band(c, :));
        threshold = mean(trace_all, 'omitnan') + ...
            params.ThresRatio_range(b) * std(trace_all, 0, 'omitnan');

        loc_pooled = zeros(0, 1);
        loc_peak = zeros(0, 1);
        event_win = zeros(0, 2);

        for k = 1:numel(session_start_idx)
            idx1 = session_start_idx(k);
            idx2 = session_end_idx(k);

            trace_seg = trace_all(idx1:idx2);
            loc_seg = find(trace_seg > threshold);
            if ~isempty(loc_seg)
                loc_seg(1) = [];
            end

            if isempty(loc_seg)
                continue;
            end

            loc_pooled = [loc_pooled; idx1 + loc_seg(:) - 1]; %#ok<AGROW>
            loc_peak_local = find_peak_loc(trace_seg, loc_seg, params.L_extract_range(b));
            if isempty(loc_peak_local)
                continue;
            end

            loc_peak_global = idx1 + loc_peak_local(:) - 1;
            n_new = numel(loc_peak_local);
            tmp_win = zeros(n_new, 2);
            for i = 1:n_new
                tmp_win(i, 1) = loc_peak_local(i) - params.L_start_range(b) + 1;
                tmp_win(i, 2) = tmp_win(i, 1) + params.L_extract_range(b) - 1;
            end

            if enforce_segment_bounds
                keep_mask = tmp_win(:, 1) >= 1 & tmp_win(:, 2) <= numel(trace_seg);
                loc_peak_global = loc_peak_global(keep_mask);
                tmp_win = tmp_win(keep_mask, :);
            end

            if isempty(tmp_win)
                continue;
            end

            tmp_win = tmp_win + idx1 - 1;
            loc_peak = [loc_peak; loc_peak_global]; %#ok<AGROW>
            event_win = [event_win; tmp_win]; %#ok<AGROW>
        end

        out = struct();
        out.threshold = threshold;
        out.loc_pooled = loc_pooled;
        out.loc_peak = loc_peak;
        out.event_win = event_win;
        out.event_count = size(event_win, 1);
        out.peak_count = numel(loc_peak);
        DetectResults{b, c} = out;
    end
end

result = struct();
result.variant_name = char(string(variant_name));
result.data_source = char(string(data_source));
result.Fs = double(Fs);
result.dx = double(dx);
result.is_session_split = ~isempty(session_lengths);
result.enforce_segment_bounds = logical(enforce_segment_bounds);
result.session_lengths = double(session_lengths(:));
result.DetectResults = DetectResults;
end


function [session_start_idx, session_end_idx] = local_resolve_session_bounds(L_total, session_lengths)
if nargin < 2 || isempty(session_lengths)
    session_start_idx = 1;
    session_end_idx = L_total;
    return;
end

session_lengths = double(session_lengths(:));
if sum(session_lengths) ~= L_total
    error('Session lengths sum to %d, but total length is %d.', sum(session_lengths), L_total);
end

session_end_idx = cumsum(session_lengths);
session_start_idx = [1; session_end_idx(1:end-1) + 1];
end


function tbl = local_build_variant_count_table(variant_results, band_labels)
rows = cell(0, 1);
for v = 1:numel(variant_results)
    DetectResults = variant_results(v).DetectResults;
    [n_bands, n_channels] = size(DetectResults);
    for b = 1:n_bands
        variant_name = repmat(string(variant_results(v).variant_name), n_channels, 1);
        band_label = repmat(string(band_labels{b}), n_channels, 1);
        channel_idx = transpose(1:n_channels);
        event_count = zeros(n_channels, 1);
        peak_count = zeros(n_channels, 1);
        for c = 1:n_channels
            event_count(c) = double(DetectResults{b, c}.event_count);
            peak_count(c) = double(DetectResults{b, c}.peak_count);
        end
        rows{end+1, 1} = table( ... %#ok<AGROW>
            variant_name, band_label, channel_idx, event_count, peak_count);
    end
end
tbl = vertcat(rows{:});
end


function tbl = local_build_peak_match_table(variant_results, reference_variant_name, band_labels)
ref_idx = find(strcmp({variant_results.variant_name}, reference_variant_name), 1, 'first');
if isempty(ref_idx)
    error('Reference variant "%s" was not found.', reference_variant_name);
end

ref_result = variant_results(ref_idx);
rows = cell(0, 1);

for v = 1:numel(variant_results)
    if v == ref_idx
        continue;
    end

    [n_bands, n_channels] = size(ref_result.DetectResults);
    for b = 1:n_bands
        for c = 1:n_channels
            ref_peaks = double(ref_result.DetectResults{b, c}.loc_peak(:));
            var_peaks = double(variant_results(v).DetectResults{b, c}.loc_peak(:));

            shared_peaks = intersect(ref_peaks, var_peaks);
            union_peaks = union(ref_peaks, var_peaks);

            if isempty(union_peaks)
                jaccard = 1;
            else
                jaccard = numel(shared_peaks) / numel(union_peaks);
            end

            rows{end+1, 1} = table( ... %#ok<AGROW>
                string(reference_variant_name), ...
                string(variant_results(v).variant_name), ...
                string(band_labels{b}), ...
                c, ...
                numel(ref_peaks), ...
                numel(var_peaks), ...
                numel(shared_peaks), ...
                numel(setdiff(ref_peaks, var_peaks)), ...
                numel(setdiff(var_peaks, ref_peaks)), ...
                jaccard, ...
                'VariableNames', { ...
                    'reference_variant', ...
                    'variant_name', ...
                    'band_label', ...
                    'channel_idx', ...
                    'ref_peak_count', ...
                    'variant_peak_count', ...
                    'shared_peak_count', ...
                    'ref_only_peak_count', ...
                    'variant_only_peak_count', ...
                    'peak_jaccard'});
        end
    end
end

tbl = vertcat(rows{:});
end


function tbl = local_build_aggregate_match_table(peak_match_table)
variant_names = unique(peak_match_table.variant_name, 'stable');
rows = cell(numel(variant_names), 1);

for i = 1:numel(variant_names)
    idx = peak_match_table.variant_name == variant_names(i);
    ref_peak_count = sum(peak_match_table.ref_peak_count(idx));
    variant_peak_count = sum(peak_match_table.variant_peak_count(idx));
    shared_peak_count = sum(peak_match_table.shared_peak_count(idx));

    recall_vs_ref = shared_peak_count / max(ref_peak_count, 1);
    precision_vs_ref = shared_peak_count / max(variant_peak_count, 1);

    rows{i} = table( ...
        variant_names(i), ...
        ref_peak_count, ...
        variant_peak_count, ...
        shared_peak_count, ...
        recall_vs_ref, ...
        precision_vs_ref, ...
        mean(peak_match_table.peak_jaccard(idx)), ...
        min(peak_match_table.peak_jaccard(idx)), ...
        max(peak_match_table.peak_jaccard(idx)), ...
        'VariableNames', { ...
            'variant_name', ...
            'ref_peak_count_total', ...
            'variant_peak_count_total', ...
            'shared_peak_count_total', ...
            'recall_vs_ref', ...
            'precision_vs_ref', ...
            'mean_peak_jaccard', ...
            'min_peak_jaccard', ...
            'max_peak_jaccard'});
end

tbl = vertcat(rows{:});
end


function tbl = local_compare_against_saved_current_result(cfg, loader, variant_results, variant_name, band_labels)
variant_idx = find(strcmp({variant_results.variant_name}, variant_name), 1, 'first');
if isempty(variant_idx)
    error('Variant "%s" was not found.', variant_name);
end

output_root = io_project.get_project_processed_root();
[R_saved, source_event_file] = io_results.load_event_results(cfg, output_root, []);
saved_result = local_extract_saved_detect_result(R_saved);
current_result = variant_results(variant_idx);

rows = cell(0, 1);
for b = 1:numel(band_labels)
    for c = 1:loader.n_channels
        saved_peaks = double(saved_result.DetectResults{b, c}.loc_peak(:));
        test_peaks = double(current_result.DetectResults{b, c}.loc_peak(:));
        shared_peaks = intersect(saved_peaks, test_peaks);
        union_peaks = union(saved_peaks, test_peaks);

        if isempty(union_peaks)
            jaccard = 1;
        else
            jaccard = numel(shared_peaks) / numel(union_peaks);
        end

        rows{end+1, 1} = table( ... %#ok<AGROW>
            string(source_event_file), ...
            string(variant_name), ...
            string(band_labels{b}), ...
            c, ...
            numel(saved_peaks), ...
            numel(test_peaks), ...
            numel(shared_peaks), ...
            jaccard, ...
            'VariableNames', { ...
                'saved_result_file', ...
                'test_variant_name', ...
                'band_label', ...
                'channel_idx', ...
                'saved_peak_count', ...
                'test_peak_count', ...
                'shared_peak_count', ...
                'peak_jaccard'});
    end
end

tbl = vertcat(rows{:});
end


function result = local_extract_saved_detect_result(R_saved)
[n_bands, n_channels] = size(R_saved.DetectResults);
DetectResults = cell(n_bands, n_channels);
for b = 1:n_bands
    for c = 1:n_channels
        src = R_saved.DetectResults{b, c};
        out = struct();
        out.loc_peak = double(src.loc_peak(:));
        out.loc_pooled = double(src.loc_pooled(:));
        out.event_win = double(src.event_win);
        out.event_count = size(out.event_win, 1);
        out.peak_count = numel(out.loc_peak);
        DetectResults{b, c} = out;
    end
end

result = struct();
result.variant_name = 'saved_event_detection';
result.DetectResults = DetectResults;
end
