function legacy = local_load_legacy_e10_data(legacy_mat_file)
S = load(legacy_mat_file);

if ~isfield(S, 'sr_lfp') || ~isfield(S, 'pl_lfp')
    error('Legacy MAT file must contain sr_lfp and pl_lfp.');
end
if ~isfield(S, 'dx_blp') || ~isfield(S, 'segL')
    error('Legacy MAT file must contain dx_blp and segL.');
end

legacy = struct();
legacy.source_name = 'legacy_prepared_mat';
legacy.state_var = [double(S.sr_lfp).'; double(S.pl_lfp).'];
legacy.dx = double(S.dx_blp);
legacy.Fs_fixed = 667;
legacy.session_lengths = double(S.segL(:));
legacy.region_labels = {'sr', 'pl'};
if isfield(S, 'sr_ch') && isfield(S, 'pl_ch')
    legacy.selected_channel_ids = [double(S.sr_ch(:)); double(S.pl_ch(:))];
else
    legacy.selected_channel_ids = transpose(1:size(legacy.state_var, 1));
end
legacy.channel_labels = local_build_channel_labels( ...
    {'sr', 'pl'}, ...
    [size(S.sr_lfp, 2), size(S.pl_lfp, 2)]);
legacy.total_samples = size(legacy.state_var, 2);
legacy.n_channels = size(legacy.state_var, 1);
end


function loader = local_load_current_e10_data(cfg)
D = io_raw.load_blp_dataset(cfg);

loader = struct();
loader.source_name = 'current_loader';
loader.state_var = transpose(double(D.data));
loader.dx_ref = median(double(D.session_dx(:)));
loader.Fs_from_dx = 1 / loader.dx_ref;
loader.session_lengths = double(D.session_lengths(:));
loader.selected_channels = double(D.selected_channels(:));
loader.channel_labels = local_build_channel_labels( ...
    cfg.channels.selected_labels(:).', ...
    [numel(cfg.channels.selected_site1), numel(cfg.channels.selected_site2)]);
loader.total_samples = size(loader.state_var, 2);
loader.n_channels = size(loader.state_var, 1);
loader.session_ids = double(D.session_ids(:));
loader.session_dx = double(D.session_dx(:));
end


function labels = local_build_channel_labels(region_labels, counts_per_region)
labels = strings(sum(counts_per_region), 1);
idx = 0;
for r = 1:numel(region_labels)
    for k = 1:counts_per_region(r)
        idx = idx + 1;
        labels(idx) = sprintf('%s_%02d', char(string(region_labels{r})), k);
    end
end
end


function tbl = local_compare_sources(legacy, loader)
if legacy.total_samples ~= loader.total_samples || legacy.n_channels ~= loader.n_channels
    tbl = table( ...
        ["legacy_prepared_mat"; "current_loader"], ...
        [legacy.total_samples; loader.total_samples], ...
        [legacy.n_channels; loader.n_channels], ...
        [legacy.dx; loader.dx_ref], ...
        'VariableNames', {'source_name', 'total_samples', 'n_channels', 'dx'});
    return;
end

rows = cell(loader.n_channels, 1);
for c = 1:loader.n_channels
    x = double(legacy.state_var(c, :));
    y = double(loader.state_var(c, :));
    diff_xy = y - x;

    rows{c} = table( ...
        c, ...
        legacy.channel_labels(c), ...
        loader.channel_labels(c), ...
        max(abs(diff_xy)), ...
        mean(abs(diff_xy)), ...
        sqrt(mean(diff_xy .^ 2)), ...
        max(abs(diff_xy)) == 0, ...
        'VariableNames', { ...
            'channel_idx', ...
            'legacy_channel_label', ...
            'loader_channel_label', ...
            'max_abs_diff', ...
            'mean_abs_diff', ...
            'rmse', ...
            'exact_match'});
    clear x y diff_xy;
end

tbl = vertcat(rows{:});
end


