fprintf('Running detector variants on current loader data, Fs inferred from median(session_dx)...\n');
loader_dx_cache = local_precompute_filtered_bands(loader.state_var, loader.Fs_from_dx, params);
variant_results(end+1) = local_detect_from_filtered( ... %#ok<SAGROW>
